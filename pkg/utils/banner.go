// GetRandomBanner 返回一个随机选择的banner图案
func GetRandomBanner() string {
	// 初始化随机数生成器
	rand.Seed(time.Now().UnixNano())

	// 随机选择一个banner
	return banners[rand.Intn(len(banners))]
}
