type ShellCode2Hex struct {
	Shellcode   string
	TargetOS    string
	TargetArch  string
	DllInjFuncs any
	Filename    string
	Path        string
}

func NewShellCode2Hex(shellcode, targetOs, targetArch string, filename, path string) *ShellCode2Hex {
	return &ShellCode2Hex{
        Shellcode: shellcode,
        TargetOS: targetOs,
        TargetArch: targetArch,
        Filename: filename,
        Path: path,
    }
}

func (sc2 *ShellCode2Hex)MkDirs() error {
    if !sc2.exists(sc2.Path) {
        return os.MkdirAll(sc2.Path, 0755)
    }
    return nil
}

func (sc2 *ShellCode2Hex) exists(path string) bool {
    _, err := os.Stat(path)
    return !os.IsExist(err)
}

